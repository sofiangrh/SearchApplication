    public void getAllDrive() {
        File roots[] = File.listRoots();
        for (int i = 0; i < roots.length; i++) {
            drive.add(roots[i].toString());
        }
    }
    
    public void search(File f) {
        try {
            String[] str = f.list();
            for (int i = 0; i < str.length; i++) {
                File fl = new File(f.getAbsolutePath(), str[i]);
                boolean bol = false;
                lblPath.setText(fl.getAbsolutePath());

                for (int j = 0; j <= str[i].length() - keyWord.length(); j++) {
                    if (str[i].substring(j, j + keyWord.length()).equalsIgnoreCase(keyWord)) {
                        bol = true;
                        break;
                    }
                }

                if (bol) {
                    Vector vec = new Vector();
                    vec.addElement(fl.getName());
                    vec.addElement(fl.getAbsolutePath());
                    defModel.addRow(vec);
                    lblFound.setText("Found: " + String.valueOf(defModel.getRowCount()));
                }
                if (fl.isDirectory() && fl.canRead()) {
                    search(fl);
                }
            }
        } catch (Exception e) {
        }
    }
    
    public void run() {
        getAllDrive();
        for (int i = 0; i < drive.size(); i++) {
            File f = new File(drive.get(i).toString());
            if (f.exists() && f.canRead()) {
                search(f);
            }
        }
        lblPath.setText("Searching is finished...");
        btnStop.setEnabled(false);
    }
    
