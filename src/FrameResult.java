    private void menuOpenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_menuOpenActionPerformed
       File folder;
        try {
            int row = tblResult.getSelectedRow();
            File f = new File((String) defModel.getValueAt(row, 1));
            if (f.isDirectory()) {
                folder = f;
            } else {
                folder = new File(new File((String) defModel.getValueAt(row, 1)).getParent());
            }
            Runtime.getRuntime().exec("explorer.exe " + folder.getAbsolutePath());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Unable To Open Folder");
        }
    }//GEN-LAST:event_menuOpenActionPerformed

